#!/usr/bin/env node
// Extract Gemini CLI OAuth credentials from @google/gemini-cli-core npm package.
// Generates api/src/generated/gemini-oauth-credentials.ts with named exports.
// Runs as postinstall and prebuild hook — see package.json.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PACKAGE_OAUTH_PATH = resolve(
  __dirname,
  '../node_modules/@google/gemini-cli-core/dist/src/code_assist/oauth2.js',
);
const OUTPUT_DIR = resolve(__dirname, '../src/generated');
const OUTPUT_FILE = resolve(OUTPUT_DIR, 'gemini-oauth-credentials.ts');

let source;
try {
  source = readFileSync(PACKAGE_OAUTH_PATH, 'utf-8');
} catch {
  // In production Docker stage, devDeps are absent. The generated file is
  // already baked into dist/index.js by esbuild, so skip silently.
  if (process.env.NODE_ENV === 'production') {
    process.exit(0);
  }
  console.error(
    `FATAL: Cannot read ${PACKAGE_OAUTH_PATH}\n` +
      'Ensure @google/gemini-cli-core is installed: npm install --save-dev @google/gemini-cli-core',
  );
  process.exit(1);
}

const clientIdMatch = source.match(/OAUTH_CLIENT_ID\s*=\s*['"]([^'"]+)['"]/);
const clientSecretMatch = source.match(/OAUTH_CLIENT_SECRET\s*=\s*['"]([^'"]+)['"]/);

if (!clientIdMatch || !clientSecretMatch) {
  console.error(
    'FATAL: Could not extract OAUTH_CLIENT_ID or OAUTH_CLIENT_SECRET from @google/gemini-cli-core.\n' +
      'The package internal structure may have changed. Manual review required.',
  );
  process.exit(1);
}

const clientId = clientIdMatch[1];
const clientSecret = clientSecretMatch[1];

if (!clientId.endsWith('.apps.googleusercontent.com')) {
  console.error(`FATAL: Extracted client ID does not look valid: ${clientId}`);
  process.exit(1);
}
if (!clientSecret.startsWith('GOCSPX-')) {
  console.error(`FATAL: Extracted client secret does not look valid: ${clientSecret}`);
  process.exit(1);
}

const content = `// AUTO-GENERATED by scripts/extract-gemini-credentials.mjs
// Source: @google/gemini-cli-core (Gemini CLI open-source OAuth credentials)
// Do NOT edit manually. Re-run: node scripts/extract-gemini-credentials.mjs

export const GEMINI_OAUTH_CLIENT_ID = '${clientId}';
export const GEMINI_OAUTH_CLIENT_SECRET = '${clientSecret}';
`;

mkdirSync(OUTPUT_DIR, { recursive: true });
writeFileSync(OUTPUT_FILE, content, 'utf-8');
console.log('Gemini OAuth credentials extracted successfully.');
